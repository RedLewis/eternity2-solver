static int e2TileIndex = 0;
TileData::TileData(){
    unsigned char first = E2TILEVALUES[e2TileIndex][0];
    unsigned char second = E2TILEVALUES[e2TileIndex][1];
    unsigned char third = E2TILEVALUES[e2TileIndex][2];
    unsigned char fourth = E2TILEVALUES[e2TileIndex][3];

    int i = 0;
    if (first == 0 && second == 0)
        i = 0;
    else if (second == 0 && third == 0)
        i = 1;
    else if (third == 0 && fourth  == 0)
        i = 2;
    else if (fourth  == 0 && first == 0)
        i = 3;
    else if (first == 0)
        i = 0;
    else if (second == 0)
        i = 1;
    else if (third == 0)
        i = 2;
    else if (fourth  == 0)
        i = 3;
    else
        i = 0;


    s_pattern[i % 4].top = first;
    s_pattern[i % 4].right = second;
    s_pattern[i % 4].down = third;
    s_pattern[i % 4].left = fourth ;
    ++i;
    s_pattern[i % 4].top = fourth ;
    s_pattern[i % 4].right = first;
    s_pattern[i % 4].down = second;
    s_pattern[i % 4].left = third;
    ++i;
    s_pattern[i % 4].top = third;
    s_pattern[i % 4].right = fourth ;
    s_pattern[i % 4].down = first;
    s_pattern[i % 4].left = second;
    ++i;
    s_pattern[i % 4].top = second;
    s_pattern[i % 4].right = third;
    s_pattern[i % 4].down = fourth ;
    s_pattern[i % 4].left = first;
    e2TileIndex = (e2TileIndex + 1) % 256;

}

